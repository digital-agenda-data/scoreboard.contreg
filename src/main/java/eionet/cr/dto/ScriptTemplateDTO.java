package eionet.cr.dto;

/**
 * Script template.
 *
 * @author Juhan Voolaid
 */
public class ScriptTemplateDTO {

    /** Id. */
    private String id;

    /** Name. */
    private String name;

    /** Script. */
    private String script;

    /**
     * @return the id
     */
    public String getId() {
        return id;
    }

    /**
     * @param id
     *            the id to set
     */
    public void setId(String id) {
        this.id = id;
    }

    /**
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * @param name
     *            the name to set
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * @return the script
     */
    public String getScript() {
        return script;
    }

    /**
     * @param script
     *            the script to set
     */
    public void setScript(String script) {
        this.script = script;
    }

}
